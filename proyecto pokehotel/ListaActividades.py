class Actividades:
    def __init__(self, nadar, paseo, fogata, sJuegos):
        self.nadar = nadar
        self.paseo = paseo
        self.fogata = fogata
        self.sJuegos = sJuegos


    def get_nadar(self):
        return self.nadar

    def get_paseo(self):
        return self.paseo

    def get_fogata(self):
        return self.fogata

    def get_sJuegos(self):
        return self.sJuegos